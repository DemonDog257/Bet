 <br>
Запрещено ломать спавнеры.
 
Несмотря на то, что воровство в целом присутствует, запрещено ломать постройки других игроков, развивайтесь честно, не мешая другим.
 <br>
Запрещено делать ловушки для игроков на их территории, около порталов, точек спавна, в центре карты, где и происходит финальное PVP. Вы можете делать ловушки ТОЛЬКО для защиты своей СОБСТВЕННОЙ территории.
 <br>
СТРОГО НАСТРОГО ЗАПРЕЩЕНО УРОДОВАТЬ (заливать водой, лавой, сжигать и вырубать растительность и что-то подобное) нулевые координаты! Ибо это потом мешает КРУТОМУ И ЧЕСТНОМУ ПВП!
