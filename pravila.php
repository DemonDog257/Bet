Правила: Первое и главное правило - постоянно проверяйте группу. Вся информация о нововведениях первым делом появляется именно здесь. </br>
 
Тиммерство запрещено. Если ваше выживание не задалось и вы, к примеру, не смогли найти деревню, то ваше право подселиться к какому-либо более развившемуся человеку, но на финальном PVP вы обязаны драться друг против друга.
 <br>
Запрещены оскорбления в чате. Относитесь ко всем людям с уважением, а индивиды, которые не могут себя адекватно вести будут, наказаны администрацией.
 <br>
Воровство ресурсов других игроков в целом НЕ запрещено, но если же вас заметят и смогут принести достаточное количество доказательств то вы получаете бан. Разблокируют вас только, когда наступит день начала движения барьера.
 <br>
Запрещены читы и софт, упрощающий или дающий преимущество во время игры. Моды, которые добавляют миникарту, полосу HP противника и иные подобные модификации тоже не приветствуются. За использование всего этот вы получите бан на текущей и ВСЕХ последующих неделях игры.
 <br>
